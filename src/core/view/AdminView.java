    private void openPatientViewButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_openPatientViewButtonActionPerformed
        String selectedPatientId = patientSelectorComboBox.getItemAt(patientSelectorComboBox.getSelectedIndex());
        if ("Select one".equals(selectedPatientId)) {
            JOptionPane.showMessageDialog(this, "Patient is required.");
            return;
        }
        Patient temp = context.getUserRepository().findById(Long.parseLong(selectedPatientId))
                .filter(Patient.class::isInstance)
                .map(Patient.class::cast)
                .orElse(null);
        if (temp == null) {
            JOptionPane.showMessageDialog(this, "Patient not found.");
            return;
        }
        PatientView patient = new PatientView(user,temp,users,appointments,hospitalizations);
        this.setVisible(false);
        patient.setVisible(true);
    }//GEN-LAST:event_openPatientViewButtonActionPerformed


