    private void loadUserCombos() {
        doctorSelectorComboBox.removeAllItems();
        patientSelectorComboBox.removeAllItems();
        doctorSelectorComboBox.addItem("Select one");
        patientSelectorComboBox.addItem("Select one");
        for (Doctor doctor : context.getUserRepository().getDoctors()) {
            doctorSelectorComboBox.addItem(String.valueOf(doctor.getId()));
        }
        for (Patient patient : context.getUserRepository().getPatients()) {
            patientSelectorComboBox.addItem(String.valueOf(patient.getId()));
        }
    }

